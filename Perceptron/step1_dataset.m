input = load('fg_inputs.txt');
output = load('fg_outputs.txt');

input(:,4) = output;
positive = input(input(:,4)==1,:);
negative = input(input(:,4)==-1,:);
figure;
plot3(positive(:,1),positive(:,2),positive(:,3),'+');
hold on;
plot3(negative(:,1),negative(:,2),positive(:,3),'o');
grid on;
ax = gca;
ax.XAxisLocation = 'origin';
ax.YAxisLocation = 'origin';
legend('Positive Classification', 'Negative Classification');
title('Plot of Input Vectors and Expected Classification');
